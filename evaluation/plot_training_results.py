import argparse
from pathlib import Path
import pandas as pd
import matplotlib.pyplot as plt
from utils.common import ensure_dir


def plot_results(csv_path: str, output_dir: str):
    csv_path = Path(csv_path)
    output_dir = ensure_dir(output_dir)
    df = pd.read_csv(csv_path)
    df.columns = [c.strip() for c in df.columns]
    metrics = [c for c in df.columns if c != "epoch"]
    for col in metrics:
        if df[col].dtype.kind not in "biufc":
            continue
        plt.figure()
        plt.plot(df["epoch"] if "epoch" in df.columns else range(len(df)), df[col])
        plt.xlabel("Epoch")
        plt.ylabel(col)
        plt.title(col)
        out = output_dir / f"{col.replace('/', '_')}.png"
        plt.savefig(out, dpi=200, bbox_inches="tight")
        plt.close()
    print(f"Plots saved to {output_dir}")


if __name__ == "__main__":
    parser = argparse.ArgumentParser(description="Plot YOLO training results.csv")
    parser.add_argument("--csv", required=True, help="Path to results.csv generated by YOLO training")
    parser.add_argument("--out", default="results/training_plots")
    args = parser.parse_args()
    plot_results(args.csv, args.out)
